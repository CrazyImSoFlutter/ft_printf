#include "ft_printf.h"

void	ft_vsprintf_i(char **tmp, va_list *ap)
{
	if (**tmp == 'i')
	{
		g_n = va_arg(*ap, int);
		g_flags |= SIGNEDFLAG;
		g_base = 10;
		g_checker = 3;
	}
}
