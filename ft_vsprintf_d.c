#include "ft_printf.h"

int		g_base;

void	ft_vsprintf_d(char *tmp)
{
	g_base = 10;
	if (*tmp == 'd')
	{
        g_checker = 1;
		g_base = 10;
		g_flags |= SIGN;
	}
}
