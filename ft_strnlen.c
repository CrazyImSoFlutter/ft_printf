#include "ft_printf.h"

int		ft_strnlen(const char *str, int n)
{
	char	*tmp;
	int		i;

	i = -1;
	tmp = (char *)str;
	while (*tmp && ++i < n)
	{
		tmp++;
	}
	return (i);
}
