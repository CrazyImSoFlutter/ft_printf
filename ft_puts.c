#include "ft_printf.h"

void	ft_puts(char *str)
{
	char	*c;
	int		i;

	i = 0;
	c = str;
	while (c[i])
	{
		if ((int)c[i] == 127)
			write(1, 0, 1);
		else
			write(1, &c[i], 1);
		i++;
	}
}
